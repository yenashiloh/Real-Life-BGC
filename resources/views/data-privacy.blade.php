            <h5 style="margin-top:10px;">The Purpose of this Privacy Policy</h5>
            <p>Every Nation Philippines values and respects your right to privacy. As we collect, use, store, and dispose information you provide us, we will faithfully steward this information and treat it with utmost respect and confidentiality.</p>
            <p>We are committed to following general principles and rules of data privacy protection in the Philippines. This policy is in compliance with Republic Act No. 10173 or the Data Privacy Act of 2012 and its implementing rules and regulations.</p>
            <p>This policy covers the collection, use, access, sharing, retention, and disposal of your information. It applies to Every Nation Leadership Institute, Every Nation Seminary, the Real LIFE Foundation, 84 Media, Victory churches in Metro Manila, and Every Nation Campus ministries throughout the Philippines, collectively known as Every Nation Philippines. As you decide how to share your personal information with us and others, we encourage you to also take the necessary precautions to protect your own privacy.</p>
            <br>
            <h5>Why We Collect Personal Information</h5>
            <p>We receive personal information for a variety of purposes and through various channels, including but not limited to, offering envelopes, registration forms, application forms, online forms, and partner information cards.</p>
            <p>We collect information so we can:</p>
            <ol>
                <li>Serve you and your family by providing worship, discipleship, or sacerdotal services;</li>
                <li>Work with our volunteers and communicate with them regarding our worship services and events;</li>
                <li>Acknowledge, update, and communicate with our ministry partners about their support;</li>
                <li>Serve our Every Nation Philippines pastors and staff, and the students of Every Nation Leadership Institute and Every Nation Seminary, and communicate with them regarding important ministry updates;</li>
                <li>Assess candidates for scholarships provided by the Real LIFE Foundation, grant scholarships, monitor academic performance, and provide assistance to the scholars and their families;</li>
                <li>Review and continuously improve our services and channels, including but not limited to, worship, discipleship, and sacerdotal services; our music and materials; our websites, social network accounts, and applications.</li>
            </ol>
            <p>We commit to using your information in ways that will allow us to work with you to honor God and make disciples.</p>
            <br>
            <h5>What type of Information We Collect</h5>
            <p>In the course of communicating with you, we may collect information, understanding that much of it is considered personal and sensitive. We remain appreciative of the trust our ministry partners and church members have given us.</p>
            <p>None of the following information we collect is automatically gathered at our worship services and events, or from any of our channels, including our social media accounts, website, and applications. We collect contact details, such as the following, only when you knowingly and voluntarily submit them to us:</p>
            <ul>
                <li>Your full legal name</li>
                <li>Your complete address</li>
                <li>Your phone numbers</li>
                <li>Your mobile numbers</li>
                <li>Your e-mail address</li>
                <li>Your birth date</li>
                <li>The contact details of your spouse or immediate family</li>
            </ul>
            <p>We, or our third-party service providers, may also collect any information sourced from communication we may have had with you, including the following information knowingly and voluntarily provided by you:</p>
            <ul>
                <li>Information on your discipleship journey, including the church you are part of, and your Victory group leader’s name and contact information;</li>
                <li>Your professional and practice information;</li>
                <li>Your health information; and</li>
                <li>Personal concerns (including, but not limited to, concerns on marriage, family, sexuality, and finances).</li>
            </ul>
            <br>    
            <h5>Kids Ministry and Campus Ministry</h5>
            <p>We are equally committed to protecting the privacy of children who are part of our Kids Ministry and campus ministry. We recognize the value of our relationships with these children and their families, and are committed to respecting and protecting their personal information. Because of this, in general, we only collect information from children to allow them to participate in our activities.</p>
            <p>It is not our policy to collect personal information from a child in Kids Ministry without a parent’s or guardian’s notice or consent. The information we gather from children and their families include, but are not limited to:</p>
            <ul>
                <li>Contact details (child’s name and birthday, parent/guardian’s name, phone numbers, mobile number, home address, and e-mail address)</li>
                <li>Secondary Information (such as level of education, school, and food allergies)</li>
                <li>Digital photographs (for event registration)</li>
            </ul>
            <p>The information we gather from children who are part of our campus ministry include, but are not limited to:</p>
            <ul>
                <li>Contact details (name and birthday, parent/guardian’s name, phone numbers, mobile number, and e-mail address)</li>
                <li>Secondary Information (such as level of education, course, school or campus)</li>
            </ul>
            <br>   
            <h5>Real LIFE Foundation</h5>
            <p>As we process and grant applications for scholarships provided by the Real LIFE Foundation, the following information is gathered from students and their families:</p>
            <ul>
                <li>Contact details (name and birthday, parent/guardian’s name, phone numbers, mobile number, home address, and e-mail address)</li>
                <li>Family history</li>
                <li>Health information</li>
                <li>Household income</li>
                <li>Educational information (school and campus information, course or year level, grades, among others)</li>
                <li>Digital photographs</li>
            </ul>
            <br>   
            <h5>Where We Get This Information</h5>
            <p>We collect information only when you knowingly and voluntarily submit them to us. In general, we collect this information through:</p>
            <ul>
                <li>Personal, face-to-face contact;</li>
                <li>Phone calls;</li>
                <li>Message channels (including, but not limited to, SMS, Viber, and Facebook Messenger);</li>
                <li>Forms (including, but not limited to, sign up sheets, tithe envelopes, volunteer cards, partner information cards, scholarship application forms, and forms for sacerdotal services);</li>
                <li>Online channels (including but not limited to, social media, official websites, and applications); and</li>
                <li>Other channels in the course of rendering our services to you.</li>
            </ul>
            <p>We may use cookies and other available technology to remember your preferences and customized settings and facilitate access to our channels. Information from cookies are not personally identifiable.</p>

            <h5>Who We Share Your Information With</h5>
            <p>We have set in place reasonable and appropriate processes and technologies to safeguard your personal information. We do not disclose or share your personal information without your permission, except when required by law or the appropriate government agencies. We do not rent, sell, or exchange your information.</p>
            <p>We may share your personal information with authorized personnel and third-party service providers, but only the information needed for them to deliver specific objectives or facilitate specific processes. These personnel and providers include:</p>
            <ul>
                <li>Authorized personnel, including but not limited to pastors, staff, and volunteers, so that they may meet your discipleship or sacerdotal needs, and help you honor God and make disciples;</li>
                <li>Third-party service providers to whom we outsource key aspects of our ministry, e.g., our service providers for payment gateway and information technology systems;</li>
                <li>Health service providers, in case of an emergency; and</li>
                <li>Government agencies, when required by law.</li>
            </ul>
            <br>
            <h5>What Rights You Have</h5>
            <p>As a data subject, you have the following rights under the Data Privacy Act:</p>
            <ul>
                <li>The right to be informed whether your personal information shall be, are being, or have been processed.</li>
                <li>The right to access your personal information.</li>
                <li>The right to correct any inaccuracies in your personal information.</li>
                <li>The right to object to the processing of your personal information.</li>
                <li>The right to erasure or blocking of your personal information.</li>
                <li>The right to data portability.</li>
                <li>The right to file a complaint before the National Privacy Commission.</li>
            </ul>
<br>
            <h5>How Long We Retain Your Information</h5>
            <p>We will retain your information only as long as necessary to fulfill the purposes for which we collected them. Your information may also be retained for a longer period as permitted or required by law.</p>

            <br>
            <h5>Updates to Our Privacy Policy</h5>
